            {
                if(rollcallbranch.equalsIgnoreCase(r.getString(3))&&(r.getString(4).equalsIgnoreCase("No")))
                {
               t.addCell(Integer.toString(i));
               t.addCell(Integer.toString(r.getInt(1)));
               t.addCell(r.getString(2).toUpperCase());
               t.addCell("  ");
               i++;
                }
        }
           
                document.add(paragraph);
                document.add(t);
