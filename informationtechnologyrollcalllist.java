                paragraph.add("                            "+selectsemester5+" Semester Acadmic Year "+year+"\n\n");
                paragraph.add("                                               "+rollcallbranch+"\n\n");
                PdfPTable t=new PdfPTable(4);
                t.setWidths(new int[]{1,3,7,2});
                paragraph.setFont(f1);
                PdfPCell c=new PdfPCell(new Paragraph("Roll No."));
                PdfPCell c1=new PdfPCell(new Paragraph("Enrollment No"));
                PdfPCell c2=new PdfPCell(new Paragraph("Name of Student"));
                PdfPCell c3=new PdfPCell(new Paragraph("Remark"));
                //c.setColspan(1);
                c.setHorizontalAlignment(Element.ALIGN_CENTER);
                t.setHeaderRows(3);
                t.addCell(c);
                t.addCell(c1);                
                t.addCell(c2);
                t.addCell(c3);
               Class.forName("net.ucanaccess.jdbc.UcanaccessDriver");
            Connection con=DriverManager.getConnection("jdbc:ucanaccess://D:\\project\\project\\project.accdb");
            String sql="SELECT Enrollment,Name,coursesemester,DC FROM registerpage";
